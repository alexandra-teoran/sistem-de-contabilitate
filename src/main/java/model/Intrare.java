package model;


public class Intrare {
	String tip_intrare;
	String data;
	int suma, id_intrare, id_sursa, id_categorie;
	
	public String getTip_intrare() {
		return tip_intrare;
	}
	public void setTip_intrare(String tip_intrare) {
		this.tip_intrare = tip_intrare;
	}
	public String getData() {
		return data;
	}
	public void setData(String data) {
		this.data = data;
	}
	public int getSuma() {
		return suma;
	}
	public void setSuma(int suma) {
		this.suma = suma;
	}
	public int getId_intrare() {
		return id_intrare;
	}
	public void setId_intrare(int id_intrare) {
		this.id_intrare = id_intrare;
	}
	public int getId_sursa() {
		return id_sursa;
	}
	public void setId_sursa(int id_sursa) {
		this.id_sursa = id_sursa;
	}
	public int getId_categorie() {
		return id_categorie;
	}
	public void setId_categorie(int id_categorie) {
		this.id_categorie = id_categorie;
	}
	
}
